package com.myapplicationdev.android.tw_listview;

public class Module {
    private String modName;
    private String icon;
    public Module(String modName, String icon) {
        this.modName = modName;
        this.icon = icon;
    }
    public String getModName() {
        return modName;
    }
    public String getIcon() {
        return icon;
    }

}
